<p class="para"> &nbsp;&nbsp;&nbsp;&nbsp;一、民间借贷的含义<br/>&nbsp;&nbsp;&nbsp;
 
民间借贷是指公民之间、公民与法人之间、公民与其它组织之间的借贷。只要双方当事人意思表示真实即可认定有效，因借贷产生的抵押相应有效，但利率不得超过人民银行规定的相关利率。民间借贷是一种直接融资渠道，银行借贷则是一种间接融资渠道。民间借贷是民间资本的一种投资渠道，是民间金融的一种形式。根据《合同法》第二百一十一条规定：“自然人之间的借款合同约定支付利息的，借款的利率不得违反国家有关限制借款利率的规定”。同时根据最高人民法院《关于人民法院审理借贷案件的若干意见》的有关规定：“民间借贷的利率可以适当高于银行的利率，但最高不得超过银行同类贷款利率的四倍”。<br/>&nbsp;&nbsp;&nbsp; <br/>&nbsp;&nbsp;&nbsp;
二、民间借贷的相关法律规定<br/>&nbsp;&nbsp;&nbsp;

1、利息规定<br/>&nbsp;&nbsp;&nbsp;
《合同法》第二百一十一条规定:“自然人之间的借款合同约定支付利息的，借款的利率不得违反国家有关限制借款利率的规定。”同时根据最高人民法院《关于人民法院审理借贷案件的若干意见》的有关规定：“民间借贷的利率可以适当高于银行的利率，但最高不得超过银行同类贷款利率的四倍（包含利率本数）。”本条同时规定：“自然人之间的借款合同对支付利息没有约定或约定不明确的，视为不支付利息。”如果公民之间的借款没有约定利息，贷款方就无权收取利息。 
关于支付利息的期限，“借款人应当按照约定的期限支付利息”。而现实生活中，公民之间的借款往往没有约定得如此明确。根据《合同法》的有关规定，借贷双方可以达成补充协议，如果双方最终仍不能确定支付利息的期限的，根据《合同法》第二百零五条规定：“借款期间不满一年的，应当在返还借款时一并支付，借款期间一年以上的，应当在每届满一年时支付，剩余期间不满一年的，应当在返还借款时一并支付”。 <br/>&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;

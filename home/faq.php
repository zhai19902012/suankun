<p class="para font_color">Q   配资误区：合约签署人是个人而不是公司？</p><br/>
<p class="para ">&nbsp;&nbsp;&nbsp;&nbsp;A    经常会遇到客户提出这种疑问：为什么你们是以个人名义跟我签署合同，而不是公司呢？简单的说，其实配资所签署的合同，属于一种民间借贷行为。而民间借贷行为是合法有效且受法律保护的。个人与个人之间的借贷，历来有之，不违法。如需了解相关民间借贷      的法律条款请参考“关于民间借贷的法律规定”。因此，我们在与客户办理配资手续时，所签署的合同是个人对个人的，合同的签署人一般都是配资公司的负责人或法人。 
</p><hr/>

<p class="para "> &nbsp;&nbsp;&nbsp;&nbsp;1、账户接近平仓线时，意味着随时可能会被配资公司强行平仓出局；如果您有一些后备资金作为支持，可以追加一些资金进来。有些股票配资客户，当即将要面临平仓线时不愿自己平仓或减仓，也不愿让股票配资公司强行平仓；如若股市行情继续走弱，股票配资客       户面临的只能是被强行平仓；因此，客户如若有后备资金，一定要及时的追加资金。
